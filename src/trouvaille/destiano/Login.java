    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("java.sql.Driver");
            Connection cn=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/Trouvaille_Destiano","root","root");
            Statement stmt=cn.createStatement();
            ResultSet rs=stmt.executeQuery("select * from login where id ='"+jTextField2.getText()+"'");
            if(rs.next())
            {
                if(rs.getString("password").equals(jTextField1.getText())){
                   
                     if(jCheckBox2.isSelected()){
                         java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                            new Home().setVisible(true);
                            }
                        });
                     }
                     else if(jCheckBox1.isSelected()){
                         java.awt.EventQueue.invokeLater(new Runnable() {
                             public void run() {
                            new AdminHome().setVisible(true);
                            }
                        });
                     }                     
                   this.dispose();
                }
            else
                jLabel2.setText("Incorrect Password!!");
            }
            else
                jLabel1.setText("Incorrect User Name!!");
            
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

